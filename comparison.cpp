#define _CRT_SECURE_NO_WARNINGS 1

#include<iostream>
using namespace std;

// == != < <= > >=
int main() {
	int a = 3;
	int b = 4;
	cout << ( a == b ) << endl;
	cout << ( a != b ) << endl;
	cout << ( a > b ) << endl;
	cout << ( a < b ) << endl;
	cout << ( a >= b ) << endl;
	cout << ( a <= b ) << endl;	
	return 0;
}